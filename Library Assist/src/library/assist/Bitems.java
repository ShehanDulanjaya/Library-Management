    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            String avail="";
        
        if(avb.isSelected()){
    avail="available";
}       
         if(navb.isSelected()){
    avail="Not available";
}       
        String ISbn = isbn.getText();
        String tit= title.getText();
        String auth = author.getText();
        Integer acc= new Integer(accesion.getText());
       

        try {
            
            Library.getlibrary().getHoldings().addBook(new Bookclass(tit, ISbn, auth, acc,avail));
            Library.write("book.txt");
            tableload();
            clear();

            JOptionPane.showMessageDialog(rootPane, "Book Successfuly added");
        } catch (Exception e) {
            System.out.println("button " +e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sea=search.getText();
        if(risbn.isSelected()){
            Integer i=new Integer(sea);
            Bookclass boo=Library.getlibrary().getHoldings().findbookfromISBN(i);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});

        }
        else if(raccess.isSelected()){
            Integer i=new Integer(sea);
            Bookclass boo=Library.getlibrary().getHoldings().findbookfromaccnum(i);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});
        }
        else if(rauthor.isSelected()){

            Bookclass boo=Library.getlibrary().getHoldings().findbookbyauthor(sea);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});
        }
        else if(rtitle.isSelected()){

            Bookclass boo=Library.getlibrary().getHoldings().findbookbytitle(sea);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Please Select Search Method");
            tableload();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

